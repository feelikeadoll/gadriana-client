// ---------------------------------------------------------
// Grid layout definition
// Each slot has: colStart, colEnd, rowStart, rowEnd (CSS grid lines, 1-based)
// The grid has 5 columns. One "unit row" = 1 track.
// ---------------------------------------------------------

// BLOCK A — row 1: 5 equal cells (rows 1)
// BLOCK B — rows 2-4:
//   Big item:  col 1-3, rows 2-4  (2 cols wide, 3 rows tall)
//   3 small:   col 3-4, col 4-5, col 5-6 — rows 2, 3, 4 (each 1 col, 1 row)
//   3×3 grid:  col 3-6 is only 3 cols wide — rethink:
//     Actually: big = col 1-3 (span 2), small col 3,4,5 → rows 2,3,4 → 9 items
//     Wait: 2 cols (big) + 3 cols (small) = 5 cols total ✓
//     Big: col 1→3, rows 2→5
//     Small right: col 3→4, col 4→5, col 5→6 (3 cols × 3 rows = 9 slots)
// BLOCK C — rows 5-9: 5×5 equal cells
// BLOCK D — rows 10-12 (mirror of B):
//   3 small left: col 1→2, 2→3, 3→4 × rows 10,11,12 (9 slots)
//   Big right: col 4→6, rows 10→13

type GridSlot = {
  colStart: number;
  colEnd: number;
  rowStart: number;
  rowEnd: number;
  isLarge: boolean;
};

// One pattern cycle = 9 rows, 35 slots
// A: 1 row  (5 items)
// B: 3 rows (1 large 2×3 + 9 small)
// C: 2 rows (10 small)
// D: 3 rows (9 small + 1 large 2×3)
const PATTERN_ROWS = 9;
const PATTERN_SLOTS = 35;

const buildPatternSlots = (): GridSlot[] => {
  const slots: GridSlot[] = [];

  // BLOCK A: row 1, 5 equal cells
  for (let col = 1; col <= 5; col++) {
    slots.push({ colStart: col, colEnd: col + 1, rowStart: 1, rowEnd: 2, isLarge: false });
  }

  // BLOCK B: rows 2-4
  slots.push({ colStart: 1, colEnd: 3, rowStart: 2, rowEnd: 5, isLarge: true });
  for (let row = 2; row <= 4; row++) {
    for (let col = 3; col <= 5; col++) {
      slots.push({ colStart: col, colEnd: col + 1, rowStart: row, rowEnd: row + 1, isLarge: false });
    }
  }

  // BLOCK C: rows 5-6, 5×2 (2 rows)
  for (let row = 5; row <= 6; row++) {
    for (let col = 1; col <= 5; col++) {
      slots.push({ colStart: col, colEnd: col + 1, rowStart: row, rowEnd: row + 1, isLarge: false });
    }
  }

  // BLOCK D: rows 7-9
  for (let row = 7; row <= 9; row++) {
    for (let col = 1; col <= 3; col++) {
      slots.push({ colStart: col, colEnd: col + 1, rowStart: row, rowEnd: row + 1, isLarge: false });
    }
  }
  slots.push({ colStart: 4, colEnd: 6, rowStart: 7, rowEnd: 10, isLarge: true });

  return slots;
};

const patternSlots = buildPatternSlots();

// Given a global item index, return its absolute grid slot (with row offset applied)
const getSlot = (index: number): GridSlot => {
  const cycle = Math.floor(index / PATTERN_SLOTS);
  const posInPattern = index % PATTERN_SLOTS;
  const base = patternSlots[posInPattern];
  const rowOffset = cycle * PATTERN_ROWS;
  return {
    ...base,
    rowStart: base.rowStart + rowOffset,
    rowEnd: base.rowEnd + rowOffset,
  };
};

type ThumbnailProps = {
  id: number;
  item: MediaItem;
  openModal: (id: number) => void;
  showMediaModal: boolean | null;
  style?: React.CSSProperties;
};

function GridThumbnail({ id, item, openModal, showMediaModal, style }: ThumbnailProps) {
  if (item.type === "video") {
    return (
      <VideoThumbnail
        id={id}
        item={item}
        openModal={openModal}
        showMediaModal={showMediaModal}
        style={style}
      />
    );
  }
  return (
    <ImgThumbnail
      id={id}
      item={item}
      openModal={openModal}
      showMediaModal={showMediaModal}
      style={style}
    />
  );
}

function HomeImgGrid(props: HomeImgGridProps) {
  const [shuffledItems] = useState<MediaItem[]>(staticShuffledItems);
  const [showMediaModal, setShowMediaModal] = useState<null | boolean>(null);
  const [selectedMediaId, setSelectedMediaId] = useState<number | null>(null);
  const [screenSize, setScreenSize] = useState<boolean>(window.innerWidth >= 768);

  useEffect(() => {
    const handleResize = () => setScreenSize(window.innerWidth >= 768);
    window.addEventListener("resize", handleResize);
    if (screenSize) setShowMediaModal(true);
    else setShowMediaModal(null);
    return () => window.removeEventListener("resize", handleResize);
  }, [screenSize]);

  const openModal = (id: number) => {
    setSelectedMediaId(id);
    document.body.style.overflow = "hidden";
  };

  const closeModal = () => {
    setSelectedMediaId(null);
    document.body.style.overflow = "auto";
  };

  const filteredItems = shuffledItems.filter((item) => {
    const matchesCategory =
      props.selectedCategory === "all" || item.category === props.selectedCategory;
    const matchesCampaign =
      props.selectedBrand === "all" || item.brand === props.selectedBrand;
    return matchesCategory && matchesCampaign;
  });

  // On mobile: fall back to single-column list (no explicit grid)
  if (!screenSize) {
    return (
      <div className="mx-4 flex flex-col gap-2 w-full">
        {filteredItems.map((item, index) => {
          if (item.type === "image") {
            return (
              <ImgThumbnail
                key={`${item.id}-${props.selectedCategory}-${props.selectedBrand}`}
                id={index}
                item={item}
                openModal={openModal}
                showMediaModal={showMediaModal}
              />
            );
          } else {
            return (
              <VideoThumbnail
                key={`${item.id}-${props.selectedCategory}-${props.selectedBrand}`}
                id={index}
                item={item}
                openModal={openModal}
                showMediaModal={showMediaModal}
              />
            );
          }
        })}
      </div>
    );
  }

  // Desktop: explicit CSS Grid layout with infinitely repeating pattern
  return (
    <div className="mx-4 w-full">
      <div
        style={{
          display: "grid",
          gridTemplateColumns: "repeat(5, 1fr)",
          gap: "8px",
        }}
      >
        {filteredItems.map((item, index) => {
          const slot = getSlot(index);

          const itemStyle: React.CSSProperties = {
            gridColumn: `${slot.colStart} / ${slot.colEnd}`,
            gridRow: `${slot.rowStart} / ${slot.rowEnd}`,
            margin: 0,
          };

          return (
            <div
              key={`${item.id}-${props.selectedCategory}-${props.selectedBrand}`}
              style={itemStyle}
            >
              {item.type === "image" ? (
                <ImgThumbnail
                  id={index}
                  item={item}
                  openModal={openModal}
                  showMediaModal={showMediaModal}
                  // Override aspect ratio for large cells so they fill the tall slot
                  style={slot.isLarge ? { aspectRatio: "unset", height: "100%" } : undefined}
                />
              ) : (
                <VideoThumbnail
                  id={index}
                  item={item}
                  openModal={openModal}
                  showMediaModal={showMediaModal}
                  style={slot.isLarge ? { aspectRatio: "unset", height: "100%" } : undefined}
                />
              )}
            </div>
          );
        })}
      </div>

      {selectedMediaId !== null && showMediaModal === true && (
        <MediaModal
          id={selectedMediaId}
          mediaItems={filteredItems}
          displayModal="block"
          openModal={openModal}
          closeModal={closeModal}
        />
      )}
    </div>
  );
}

export default HomeImgGrid;